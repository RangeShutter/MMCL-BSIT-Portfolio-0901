public class PrimitiveDataTypesExample {
    public static void main(String[] args) {
        // Primitive Data Types
        byte myByte = 127;                          // 8-bit signed integer
        short myShort = 32767;                      // 16-bit signed integer
        int myInt = 2147483647;                     // 32-bit signed integer
        long myLong = 9223372036854775807L;         // 64-bit signed integer (note the 'L' suffix)
        float myFloat = 3.14f;                      // 32-bit floating point (note the 'f' suffix)
        double myDouble = 2.71828;                  // 64-bit floating point
        char myChar = 'A';                          // 16-bit Unicode character
        boolean myBoolean = true;                   // true or false

        // Printing Primitive Data Types
        System.out.println("byte: " + myByte);
        System.out.println("short: " + myShort);
        System.out.println("int: " + myInt);
        System.out.println("long: " + myLong);
        System.out.println("float: " + myFloat);
        System.out.println("double: " + myDouble);
        System.out.println("char: " + myChar);
        System.out.println("boolean: " + myBoolean);
    }
}
